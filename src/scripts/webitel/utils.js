    function prettysize(size, nospace, one) {
        var mysize, f;

        sizes.forEach(function(f, id) {
            if (one) {
                f = f.slice(0, 1);
            }
            var s = Math.pow(1024, id),
                fixed;
            if (size >= s) {
                fixed = String((size / s).toFixed(1));
                if (fixed.indexOf('.0') === fixed.length - 2) {
                    fixed = fixed.slice(0, -2);
                }
                mysize = fixed + (nospace ? '' : ' ') + f;
            }
        });

        // zero handling
        // always prints in Bytes
        if (!mysize) {
            f = (one ? sizes[0].slice(0, 1) : sizes[0]);
            mysize = '0' + (nospace ? '' : ' ') + f;
        }

        return mysize;
    }

    function secondsToString(seconds, skipHours){
        function pad(ss){
            return (ss < 10 ? '0' : '') + ss;
        }
        var d, h, m, s, str = '';
        s = Math.floor(seconds);
        m = Math.floor(s / 60);
        s = s % 60;
        h = Math.floor(m / 60);
        m = m % 60;
        d = Math.floor(h / 24);
        h = h % 24;

        if (d > 0)
            str = d + ' days ';

        if (!skipHours || h > 0)
            str += pad(h) + ':';

        return str + pad(m) + ':' + pad(s);
    }

    function WebitelHashCollection() {
        var collection = {};

        var length = 0;

        var onAddedElement = new WebitelEvent();
        var onRemovedElement = new WebitelEvent();

        var addElement = function(key, element) {
            if (collection[key]) {
                throw new Error('Key ' + key + ' already defined!');
            } else {
                collection[key] = element;
                length++;
                onAddedElement.trigger(collection[key].getJSONObject ? collection[key].getJSONObject() : collection[key]);
            };
        };

        var getLength = function() {
            return length;
        };

        var getElement = function(key) {
            return collection[key]
        };

        var removeElement = function(key) {
            if (collection[key]) {
                var removedElement = collection[key];
                delete collection[key];
                length--;
                onRemovedElement.trigger(removedElement.getJSONObject ? removedElement.getJSONObject() : removedElement);
            };
        };

        var removeAllElement = function() {
            for (var key in collection) {
                removeElement(key);
            }
        };

        var setNewKey = function(key, newKey) {
            if (collection[key]) {
                throw new Error('Key ' + key + ' not found!');
            } else {
                var element = collection[key];
                collection[newKey] = element;
                collection[key] = undefined;
                delete collection[key];
            };
        };

        return {
            add: addElement,
            get: getElement,
            remove: removeElement,
            removeAll: removeAllElement,
            setNewKey: setNewKey,
            onAdded: onAddedElement,
            onRemoved: onRemovedElement,
            length: getLength,
            collection: collection
        };
    };

